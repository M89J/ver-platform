let currentLang = 'en';

function setLanguage(lang) {
    currentLang = lang;
    const strings = I18N[lang] || I18N.en;

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (strings[key]) {
            el.textContent = strings[key];
        }
    });

    // Update specific elements
    document.getElementById('app-title').textContent = strings.app_title;
    document.getElementById('app-subtitle').textContent = strings.app_subtitle;
    document.getElementById('search-input').placeholder = strings.search_placeholder;

    // Update language button states
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

function t(key) {
    return (I18N[currentLang] || I18N.en)[key] || key;
}
